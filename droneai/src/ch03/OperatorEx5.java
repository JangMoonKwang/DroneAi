package ch03;

class OperatorEx5 {
    // Your program begins with a call to main()
    
    public static void main(String[] args)
    {
    	int 	a = 10;
    	int     b = 4;
    	

       	System.out.printf("%d + %d = %d%n", a, b, a + b);
       	System.out.printf("%d - %d = %d%n", a, b, a - b);
       	System.out.printf("%d * %d = %d%n", a, b, a * b);
       	System.out.printf("%d / %d = %d%n", a, b, a / b);
       	System.out.printf("%d / %f = %f%n", a, (float)b, a / (float)b);
       	

    }
	
	
	
}
