package ch04;


class FlowEx21 {
    // Your program begins with a call to main()
    
    // Your program begins with a call to main()
    
    public static void main(String[] args)
    {


       	for	(int i=1; i<=5; i++) {
        	for	(int j=1; j<=5; j++) {
        		System.out.print(" *");
        		System.out.printf("[%d, %d]",i,j);
        	}
    		System.out.println();
    	}
    }   	
}
